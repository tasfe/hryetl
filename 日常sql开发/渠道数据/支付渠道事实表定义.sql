COMMENT ON COLUMN DW_PAY_CHANNEL_FACT.COMMUNICATE_TYPE_CODE IS '会话类型编码';
COMMENT ON COLUMN DW_PAY_CHANNEL_FACT.COMMUNICATE_TYPE_NAME IS '会话类型名称';
COMMENT ON COLUMN DW_PAY_CHANNEL_FACT.FUND_CHANNEL_API IS '资金渠道API';
COMMENT ON COLUMN DW_PAY_CHANNEL_FACT.RISK_STATUS_CODE IS '风控状态编码';
COMMENT ON COLUMN DW_PAY_CHANNEL_FACT.RISK_STATUS_NAME IS '风控状态';
