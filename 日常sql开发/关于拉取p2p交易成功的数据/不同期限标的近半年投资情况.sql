select
S1.DAY_BORROW_DURATION 投资期限
,COUNT(*) 投资总笔数
,COUNT(DISTINCT S0.F03) 投资总人数
,SUM(S0.F05) 投资总金额
from BIDATA.S_S62_T6250 S0
LEFT JOIN DIM_PROD_P2P_LIST S1
ON S0.F02=S1.BID
WHERE S0.F07='F'
AND S0.F06 < TO_DATE('20170417','YYYYMMDD') AND S0.F06 >= TO_DATE('20161017','YYYYMMDD')
GROUP BY S1.DAY_BORROW_DURATION
ORDER BY S1.DAY_BORROW_DURATION
;